    private void jMenuItemDotFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemDotFileActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.dir")));
        int returnValue = fileChooser.showSaveDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
	    File selectedFile = fileChooser.getSelectedFile();
	    if(selectedFile.exists()) {
		int answer = JOptionPane.showConfirmDialog(
		    new JFrame(), "File exists! Overwrite ?",
		    "Warning",
		    JOptionPane.YES_NO_OPTION);
		if(answer != JOptionPane.YES_OPTION)
		    return;
	    }
	    Calculator c;
	    try{
		c = new Calculator(input);
		try (PrintWriter pfile = new PrintWriter(selectedFile)) {
		    pfile.println(c.toDotString());
		    System.out.println("PRINT DOT FILE OK!");
		}
		catch(FileNotFoundException ex) {
		    System.err.println("Unable to write dotString!!!");
		    System.exit(1);
		}
	    }
	    catch(IllegalArgumentException ex){
		System.out.println("Invalid input. Try again.");
	    }
        }
    }//GEN-LAST:event_jMenuItemDotFileActionPerformed

